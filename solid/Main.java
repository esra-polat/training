/*

SOLID PRINCIPLE

Single Responsibility => a class should only have one responsibility
Open/Closed => classes should be open for extension but closed for modification
Liskov Substitution => objects of a superclass should be replaceable with objects of its subclasses without breaking the system
                    => if class A is a subtype of class B, we should be able to replace B with A without disrupting the behavior of our program
Interface Segregation => larger interfaces should be split into smaller ones
                    => by doing so, we can ensure that implementing classes only need to be concerned about the methods that are of interest to them.
Dependency Inversion => refers to the decoupling of software modules


*/